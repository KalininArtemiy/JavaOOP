//Class owner creating
public class owner {
    private String name;
//Giving a name to our owner
    public owner(String name){
        this.name = name;
    }
    //можем задать имя
    public void setName(String name) {
        this.name = name;
    }
    //можем азять имя
    public String getName() {
        return name;
    }
}
